//============================================================================================
// 垂直帰線期間待ち処理
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//--------------------------------------------------------------------------------------------
// 参考資料：
// Inside X68000(P.81)図...3(※非公式正誤表確認済)
// MFPのGPIP(0xE88001)のbit4(V-DISP):CRTCのV-DISP信号の状態
// 0:V-DISP信号が'L'(垂直帰線期間)
// 1:V-DISP信号が'H'(垂直表示期間)
//
// 参考資料：
// ケイのホビールーム AS(アセンブラ)入門ハードウェア編#3 (6:04)～
// https://youtu.be/UstdAoyDtPY?si=RqbJu5w4ml5JgQ6B&t=364
//
// ループ 1：既にbit4が'0'(垂直帰線期間)なら'1'(垂直表示期間)まで待つ
// ループ 2：bit4が'1'(垂直表示期間)なら、'0'(垂直帰線期間)まで待つ
//
// 以上の処理でループ2を抜けた瞬間が垂直帰線期間に入った瞬間と
// なり、垂直帰線期間中に描画を完了できる可能性が高まる。
//============================================================================================
VD vdf_g_CrtVblank(VD)
{
    // 0x10 → MFP-GPIP_Bit4_V-DISP
    // ※：どちらのループもU1_MFP_GPIPの定義にvolatileが必須である事を確認済み
    while(!(U1_MFP_GPIP & 0x10)); // ループ1：垂直表示期間まで待つ
    while(U1_MFP_GPIP & 0x10);    // ループ2：垂直帰線期間まで待つ
}

//============================================================================================
// 特殊解像度(384*256)設定をを行う
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//--------------------------------------------------------------------------------------------
// 参考資料：
// Secret of Video Architecture PRO-68K
// https://channel.beep-shop.com/products/detail/38802
//
// CRTCほかレジスタ設定値について自力算出を試みたものの困難であったため、
// 上記資料に添付されているサンプルコードの設定値を使わせていただきました。
// Inside X68000にて再検証し、自前コーディング規約に則った記述に変更させていただいております。
//
// Inside X68000 非公式正誤表：
// https://kg68k.github.io/InsideX68000-errata/InsideX68000.html
//
// CRTC R03 の設定値の算出法, CRTC R20 P.233 図54 ほか
// R03, R20 に関する誤記についての記載を確認済み。
//--------------------------------------------------------------------------------------------
VD vdf_g_Crt384x256(VD)
{
    // CRTC：水平タイミング制御 (Inside P.230)
    U2_CRTC_R00 = 0x0045;  // 水平トータル
    U2_CRTC_R01 = 0x0006;  // 水平同期終了位置
    U2_CRTC_R02 = 0x000B;  // 水平表示開始位置
    U2_CRTC_R03 = 0x003B;  // 水平表示終了位置

    // CRTC：表示モード制御 (Inside P.233)
    U2_CRTC_R20 = 0x0311;

    // システムポート#4：キーボード/NMI/ドットクロック (Inside P.519)
    U1_SYSP_R04 = 0x0A;    // bit1:ドットクロック='1'(bit3も'1'にしてキーボード使用可能にすること)

    // スプライトコントローラ：画面モードレジスタ(Inside P.235)
    U2_SPC_M_R00 = 0x0045; // 水平トータル
    U2_SPC_M_R01 = 0x000F; // 水平表示位置
    U2_SPC_M_R03 = 0x0011; // 解像度設定
}
