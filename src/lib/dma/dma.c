//============================================================================================
// DMA初期化処理
//--------------------------------------------------------------------------------------------
// Arguments : const VD* const vcpc_src : (I) 転送元のアドレス
//             const VD* const vcpc_dst : (I) 転送先のアドレス
//             U1              u1_mac   : (I) 転送毎のアドレス増減設定(転送元)
//             U1              u1_dac   : (I) 転送毎のアドレス増減設定(転送先)
//             U1              u1_dsp   : (I) デバイスポートサイズ
//             U1              u1_size  : (I) 転送サイズ
//             U2              u2_cnt   : (I) 転送回数
// Return    : -
//============================================================================================
VD vdf_g_DmaInit(const VD* const vcpc_src,
                 const VD* const vcpc_dst,
                 U1 u1_mac, U1 u1_dac, U1 u1_dsp, U1 u1_size, U2 u2_cnt)
{
    // 引数で受け取ったアドレス増減設定をSCRレジスタのビット位置に合わせる
    U1 u1_scr = ((u1_mac & 0x03) << 2) | (u1_dac & 0x03);

    // DMAC使用前に必ずフラグをクリア
    U1_DMAC2_CSR = 0xFF;

    // 第3話で決めた設定値を各レジスタに書き込む
    U1_DMAC2_DCR = u1_dsp;        // 通常はDPS16を指定する(DPS8はZが対応していないため非推奨)
    U1_DMAC2_OCR = u1_size;       // [固定]MAR→DAR, オートリクエスト最大速度 [可変]転送サイズ
    U1_DMAC2_SCR = u1_scr;        // MARとDARに設定したアドレスを転送度に増減させるかの設定
    U1_DMAC2_CCR = 0x00;          // 転送開始ビットのクリア、及び未使用の機能を停止させる
    U2_DMAC2_MTC = u2_cnt;        // 転送回数
    VP_DMAC2_MAR = (VD*)vcpc_src; // 転送元アドレス
    VP_DMAC2_DAR = (VD*)vcpc_dst; // 転送先アドレス
    U1_DMAC2_MFC = 0x05;          // MARはスーパーバイザでアクセスで固定
    U1_DMAC2_CPR = 0x03;          // 優先度最低で固定
    U1_DMAC2_DFC = 0x05;          // DARはスーパーバイザでアクセスで固定
}

//============================================================================================
// DMA転送開始処理
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//============================================================================================
VD vdf_g_DmaStart(VD)
{
    U1_DMAC2_CCR |= 0x80;
}

//============================================================================================
// DMA転送完了待ち
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : BL : (O) TRUE  : 転送完了
//                      FALSE : エラー発生
//============================================================================================
BL blf_g_DmaWaitCmp(VD)
{
    while(!(U1_DMAC2_CSR & 0x90)); // ※：U1_DMAC2_CSRの定義にvolatileが必須である事を確認済み

    return !(U1_DMAC2_CSR & 0x10);
}

//============================================================================================
// DMAフラグクリア
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//============================================================================================
VD vdf_g_DmaClearFlag(VD)
{
    U1_DMAC2_CSR = 0xFF;
}

//============================================================================================
// DMAフラグを返す
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : U1 : (O) CSRの値をそのまま返す
//============================================================================================
U1 u1f_g_DmaGetFlag(VD)
{
    return U1_DMAC2_CSR;
}

//============================================================================================
// DMAエラーコードを返す
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : U1 : (O) CERの値をそのまま返す
//============================================================================================
U1 u1f_g_DmaGetErrCode(VD)
{
    return U1_DMAC2_CER;
}

