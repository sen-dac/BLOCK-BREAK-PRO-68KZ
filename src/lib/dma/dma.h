//--------------------------------------------------------------------------------------------
// 定数定義
//--------------------------------------------------------------------------------------------
// vdf_g_DmaInit()のパラメータ
#define DMA_DSP8     0x00 // 8ビットポート
#define DMA_DSP16    0x08 // 16ビットポート(標準設定)

#define DMA_SIZE1    0x01 // 転送サイズ1バイト(MAR→DAR,オートリクエスト最大速度で固定)
#define DMA_SIZE2    0x11 // 転送サイズ2バイト(MAR→DAR,オートリクエスト最大速度で固定)
#define DMA_SIZE4    0x21 // 転送サイズ4バイト(MAR→DAR,オートリクエスト最大速度で固定)

#define DMA_ADDR_FIX 0x00 // 転送毎アドレスは固定
#define DMA_ADDR_INC 0x01 // 転送毎アドレスが増加
#define DMA_ADDR_DEC 0x02 // 転送毎アドレスが減少

//--------------------------------------------------------------------------------------------
// 型定義
//--------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------
// extern
//--------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------
// プロトタイプ宣言
//--------------------------------------------------------------------------------------------
VD vdf_g_DmaInit(const VD* const vcpc_src, // DMA初期化処理
                 const VD* const vcpc_dst,
                 U1 u1_mac,
                 U1 u1_dac,
                 U1 u1_dsp,
                 U1 u1_size,
                 U2 u2_cnt);
VD vdf_g_DmaStart(VD);                     // DMA転送開始
BL blf_g_DmaWaitCmp(VD);                   // DMA転送完了待ち
VD vdf_g_DmaClearFlag(VD);                 // DMAフラグクリア
U1 u1f_g_DmaGetFlag(VD);                   // DMAフラグを返す
U1 u1f_g_DmaGetErrCode(VD);                // DMAエラーコードを返す

#endif // _DMA_H_
