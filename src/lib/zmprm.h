// m_pcmplay()のパラメータ
// m_pcmplay(nt,pn,fr)
// 機能：
// 登録してあるノート番号ntのADPCM音を鳴らす
//
// 引数：
// nt = ノート番号 [int:0～511]
// ntの定数はアプリ層にて定義

// pn = パンポット(音の左右の定位) [char:0～3]
#define PCM_PAN_N  0 // 左右共に鳴らない
#define PCM_PAN_L  1 // 左側のみ鳴る
#define PCM_PAN_R  2 // 右側のみ鳴る
#define PCM_PAN_C  3 // 左右ともに鳴る (通常これを指定する)

// fr = 再生周波数 [char:0～4]
#define PCM_FR03_9KHZ  0 // 3.9kHz
#define PCM_FR05_2KHZ  1 // 5.2kHz
#define PCM_FR07_8KHZ  2 // 7.8kHz
#define PCM_FR10_4KHZ  3 // 10.4kHz
#define PCM_FR15_6KHZ  4 // 15.6kHz (通常これを指定する)

