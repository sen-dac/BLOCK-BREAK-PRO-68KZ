//--------------------------------------------------------------------------------------------
// 定数定義 (内部用)
//--------------------------------------------------------------------------------------------
// スプライト・スクロールレジスタ
#define STP_SSCR ((volatile stSSR*)(&U2_SPC_SPSCR)) // 構造体配列としてアクセスを行う
#define SP_SSCR_MAX 128                             // レジスタ(プレーン)数 (0〜127)

// スクリーン座標→スプライト座標オフセット
// 表示画面左上(スクリーン座標:X=0,Y=0)から更にX-16,Y-16の位置がSP座標基準点(X=0,Y=0)
// スプライト描画関数外まではスクリーン座標(-16〜0〜)で扱う。
// スプライト描画関数内で変換(+SP_OFFSET)してレジスタ(U2アクセス10ビット)に書き込む。
#define SP_OFFSET   16

// BGデータエリア要素素(ハード仕様にて固定の数：Inside X68000 P.173)
#define BG_EL_W     64                              // 横の要素数
#define BG_EL_H     64                              // 縦の要素数
#define BG_EL_MAX   (BG_EL_W * BG_EL_H)             // 全要素数

// BGデータエリアの数(BG0, BG1)
#define BG_DATAREA_MAX 2

//--------------------------------------------------------------------------------------------
// 型定義 (内部用)
//--------------------------------------------------------------------------------------------
// スプライト・スクロールレジスタの構造 (Inside X68000 P.179)
typedef struct
{
    U2 u2_x;    // リザーブ(6bit), X座標(10bit：0-1023)
    U2 u2_y;    // リザーブ(6bit), Y座標(10bit：0-1023)
    U2 u2_ptc;  // V反転(1bit), H反転(1bit), リザーブ(2bit)パレット番号(4bit), PCG番号(8bit)
    U2 u2_prw;  // リザーブ(13bit), '0'(1bit), 描画優先度(2bit)
}
stSSR, *stpSSR;

//--------------------------------------------------------------------------------------------
// プロトタイプ宣言 (static)
//--------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------
// グローバル変数定義 (外部公開)
//--------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------
// 静的変数定義 (static)
//--------------------------------------------------------------------------------------------
// BGデータエリア
static volatile U2* const u2cpa_s_SpBgDataAreaTbl[BG_DATAREA_MAX] =
{
    &U2_PCG_BGAR0, // BGデータエリア0の先頭アドレス
    &U2_PCG_BGAR1, // BGデータエリア1の先頭アドレス
};

//--------------------------------------------------------------------------------------------
// 関数定義
//--------------------------------------------------------------------------------------------
//============================================================================================
// BMP → PCG 変換 (16*16サイズのタイルを一枚だけを扱う)
//--------------------------------------------------------------------------------------------
// Arguments : U4* const       u4cp_dstpcg  : (O) 出力バッファ(PCG配列)
//             const U4* const u4cpc_srcbmp : (I) 入力バッファ(BMP配列)
// Return    : -
//--------------------------------------------------------------------------------------------
// Note      :
// 入力バッファ、出力バッファ共にU4型にて最低32要素(128バイト)以上必須
// PCG1枚は16ドット*16ドット=256ドット, 1ドット=1ニブル(4ビット), 256ドット*4ビット=1024ビット
// 1024ビット/8=128バイト, U4型(4バイト)*32要素=128バイト
//
// バッファが必要サイズを確保できているかチェックを行っていないので注意。
// 本来は、バッファとその容量をセットにした構造体を用いる方が良い。
// しかし、X68000環境(リソース容量や動作速度)を考慮して単純なポインタ渡しの方針としている。
//============================================================================================
VD vdf_g_SpBmpToPcg(U4* const u4cp_dstpcg, const U4* const u4cpc_srcbmp)
{
    S4 s4_i;
    S4 s4_index = 0;

    // BMPデータの並びをPCGの並びに入れ替える(参考：Inside X68000 P.176 図...8)
    // BMP / PCG はどちらも 1ドット = 1ニブル で PCG一枚の16*16サイズだと 128バイトになる
    // 4バイト(横8ドット1ライン)を 2 * 16回 書き込む
    for(s4_i = 0; s4_i < 16; s4_i ++)
    {
        u4cp_dstpcg[s4_i +  0] = u4cpc_srcbmp[s4_index + 0];
        u4cp_dstpcg[s4_i + 16] = u4cpc_srcbmp[s4_index + 1];
        s4_index += 2;
    }
}

//============================================================================================
// PCG及びSP/BGレジスタへのアクセス高速化 ON  (SP/BGが表示不可になる)
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//--------------------------------------------------------------------------------------------
// Note      :
// Inside X68000 (P.190)...図18 の記載に基き実装 〜 効果未検証
// SP_INIT()呼出し後は高速アクセスOFF(表示ON)に設定されている
//============================================================================================
VD vdf_g_SpFastOn(VD)
{
    U2_SPC_BGCTR &= 0xFDFF; // bit9:DISP/CPU = 0 (スプライト/BG 表示OFF)
}

//============================================================================================
// PCG及びSP/BGレジスタへのアクセス高速化 OFF  (SP/BGが表示可能になる)
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//--------------------------------------------------------------------------------------------
// Note      :
// Inside X68000 (P.190)...図18 の記載に基き実装 〜 効果未検証
// SP_INIT()呼出し後は高速アクセスOFF(表示ON)に設定されている
//============================================================================================
VD vdf_g_SpFastOff(VD)
{
    U2_SPC_BGCTR |= 0x0200; // bit9:DISP/CPU = 1 (スプライト/BG 表示ON)
}

//============================================================================================
// 指定したプレーン番号のスプライト表示を消す
//--------------------------------------------------------------------------------------------
// Arguments : U1 u1_pl  : (I) プレーン番号 (0〜127)
// Return    : -
//============================================================================================
VD vdf_g_SpHide(U1 u1_pl)
{
    u1_pl &= (SP_SSCR_MAX - 1); 

    STP_SSCR[u1_pl].u2_x   = 0;
    STP_SSCR[u1_pl].u2_y   = 0;
    STP_SSCR[u1_pl].u2_ptc = 0;
    STP_SSCR[u1_pl].u2_prw = 0;
}

//============================================================================================
// スプライト描画
//--------------------------------------------------------------------------------------------
// Arguments : U1 u1_pl  : (I) プレーン番号 (0〜127)
//             S2 s2_x   : (I) スクリーン座標 X
//             S2 s2_y   : (I) スクリーン座標 Y
//             U1 u1_v   : (I) V反転 (0/1)
//             U1 u1_h   : (I) H反転 (0/1)
//             U1 u1_pb  : (I) パレット番号 (0〜15)
//             U1 u1_pcg : (I) PCG番号 (0〜255)
//             S4 u1_prw : (I) 描画優先度 (0〜3)
// Return    : -
//============================================================================================
VD vdf_g_SpRegst(U1 u1_pl, S2 s2_x, S2 s2_y, U1 u1_v, U1 u1_h, U1 u1_pb, U1 u1_pcg, U1 u1_prw)
{
    U2 u2_ptc = 0;

    u2_ptc = (((U2)u1_v  &  1) << 15) |                 // BIT:15   V反転 (0/1)
             (((U2)u1_h  &  1) << 14) |                 // BIT:14   H反転 (0/1)
             (((U2)u1_pb & 15) <<  8) |                 // BIT:8-11 パレット番号 (0〜15)
             (U2) u1_pcg;                               // BIT:0-7  PCG番号 (0〜255)

    u1_pl &= (SP_SSCR_MAX - 1);                         // プレーン番号範囲(0〜127)ガード

    STP_SSCR[u1_pl].u2_x   = (s2_x + SP_OFFSET) & 1023; // BIT:0-9 X座標 (0〜1023)
    STP_SSCR[u1_pl].u2_y   = (s2_y + SP_OFFSET) & 1023; // BIT:0-9 Y座標 (0〜1023)
    STP_SSCR[u1_pl].u2_ptc = u2_ptc;                    // パターンコード
    STP_SSCR[u1_pl].u2_prw = u1_prw & 3;                // BIT:0-1 描画優先度 (0〜3)
}

//============================================================================================
// BG0 表示 ON
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//============================================================================================
VD vdf_g_SpBg0dispOn(VD)
{
    U2_SPC_BGCTR |= 0x0001; // bit0:BG0ON = 1
}

//============================================================================================
// BG0 表示 OFF
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//============================================================================================
VD vdf_g_SpBg0dispOff(VD)
{
    U2_SPC_BGCTR &= 0xFFFE; // bit0:BG0ON = 0
}

//============================================================================================
// BG0 データエリア指定 (0 / 1)
//--------------------------------------------------------------------------------------------
// Arguments : U2 u2_txsel : (I) 0 : BG0 は BGデータエリア0を使用
//                               1 : BG0 は BGデータエリア1を使用
// Return    : -
//============================================================================================
VD vdf_g_SpBg0Txsel(U2 u2_txsel)
{
    U2_SPC_BGCTR &=  0xFFF9;   // bit1, 2(BG0 TXSEL)をクリア
    u2_txsel     &=  0x0001;   // 引数から1bitのみ抽出(0/1しか受け付けない)
    u2_txsel     <<= 1;        // bit1, 2(BG0 TXSEL)の位置にシフト
    U2_SPC_BGCTR |=  u2_txsel; // レジスタ反映
}

//============================================================================================
// BG1 表示 ON
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//============================================================================================
VD vdf_g_SpBg1dispOn(VD)
{
    U2_SPC_BGCTR |= 0x0008; // bit3:BG1ON = 1
}

//============================================================================================
// BG1 表示 OFF
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//============================================================================================
VD vdf_g_SpBg1dispOff(VD)
{
    U2_SPC_BGCTR &= 0xFFF7; // bit3:BG1ON = 0
}

//============================================================================================
// BG1 データエリア指定 (0 / 1)
//--------------------------------------------------------------------------------------------
// Arguments : U2 u2_txsel : (I) 0 : BG1 は BGデータエリア0を使用
//                               1 : BG1 は BGデータエリア1を使用
// Return    : -
//============================================================================================
VD vdf_g_SpBg1Txsel(U2 u2_txsel)
{
    U2_SPC_BGCTR &=  0xFFCF;   // bit4, 5(BG1 TXSEL)をクリア
    u2_txsel     &=  0x0001;   // 引数から1bitのみ抽出(0/1しか受け付けない)
    u2_txsel     <<= 4;        // bit4, 5(BG1 TXSEL)の位置にシフト
    U2_SPC_BGCTR |=  u2_txsel; // レジスタ反映
}

//============================================================================================
// パターンコード生成
//--------------------------------------------------------------------------------------------
// Arguments : U1 u1_v   : (I) 垂直(上下)反転(0/1)
//             U1 u1_h   : (I) 水平(左右)反転(0/1)
//             U1 u1_pb  : (I) パレットブロックNo.(0-15)
//             U1 u1_pcg : (I) PCGパターンNo.(0-255)
// Return    : U2 u2_ptc : (O) 生成されたパターンコード
//============================================================================================
U2 u2f_g_SpMkPtc(U1 u1_v, U1 u1_h, U1 u1_pb, U1 u1_pcg)
{
    U2 u2_ptc = 0;

    u2_ptc = (((U2)u1_v  &  1) << 15) | // BIT:15   V反転 (0-1)
             (((U2)u1_h  &  1) << 14) | // BIT:14   H反転 (0-1)
             (((U2)u1_pb & 15) <<  8) | // BIT:8-11 パレット番号 (0-15)
             (U2)u1_pcg;                // BIT:0-7  PCG番号 (0-255)

    return u2_ptc;
}

//============================================================================================
// BG書込み (消去の場合：ptc=0)
//--------------------------------------------------------------------------------------------
// Arguments : U1 u1_area : (I) BGデータエリア (0/1)
//             U2 u2_x    : (I) 書込み位置のX要素 (0〜63)
//             U2 u2_y    : (I) 書込み位置のY要素 (0〜63)
//             U2 u2_ptc  : (I) パターンコード (u2f_g_SpMkPtc()にて生成されたもの)
// Return    : -
//============================================================================================
VD vdf_g_SpBgSet(U1 u1_area, U2 u2_x, U2 u2_y, U2 u2_ptc)
{
    // 引数に応じたBGデータエリア(0/1)の先頭アドレスを取得
    volatile U2* const u2cp_area = u2cpa_s_SpBgDataAreaTbl[u1_area & 0x01];

    // BGデータ(ptc)をセット
    u2cp_area[(u2_y & (BG_EL_H - 1)) * BG_EL_W + (u2_x & (BG_EL_W - 1))] = u2_ptc;
}
