//============================================================================================
// 各描画リソース(GR/TX/SPBG)間の表示優先順位を指定
//--------------------------------------------------------------------------------------------
// Arguments : U2 u2_1st : (I) 最前面に表示する描画リソース
//             U2 u2_2nd : (I) 中間に表示する描画リソース
//             U2 u2_3rd : (I) 最背面に表示する描画リソース
// Return    : -
//--------------------------------------------------------------------------------------------
// Note      : 
// 引数(u2_1st～u2_3rd)は、全て以下のマクロでの入力が必須となる。
// DR_PRI_GR    // グラフィック面
// DR_PRI_TX    // テキスト面
// DR_PRI_SP    // スプライト & BG面
//============================================================================================
VD vdf_g_DrSurfacePri(U2 u2_1st, U2 u2_2nd, U2 u2_3rd)
{
    U2 u2_set = U2_VDCTR_R01;           // 現在のレジスタ値を一旦受け取る

    u2_set &= 0x00FF;                   // 下位バイトはGR間の優先度なので残す
    u2_set |= (((U2)0) << u2_1st);      // 優先度 1位(b00)を指定箇所にシフト
    u2_set |= (((U2)1) << u2_2nd);      // 優先度 2位(b01)を指定箇所にシフト
    u2_set |= (((U2)2) << u2_3rd);      // 優先度 3位(b10)を指定箇所にシフト
    U2_VDCTR_R01 = u2_set;              // 値の加工が終わってからレジスタに書き戻す
}
