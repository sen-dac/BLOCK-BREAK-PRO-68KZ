#define TXT1_ADDR    ((U2*)(&U2_VRAM_TXT1)) // T1(bit:1) 先頭アドレス
#define TXT2_ADDR    ((U2*)(&U2_VRAM_TXT2)) // T2(bit:2) 先頭アドレス
#define TXT3_ADDR    ((U2*)(&U2_VRAM_TXT3)) // T3(bit:3) 先頭アドレス

