#define TX_DOTS_W    1024                   // 画面の横幅(ドット) どの画面モードでも常に1024
#define TX_DOTS_H    1024                   // 画面の縦幅(ドット）どの画面モードでも常に1024
#define TX_XLINE_U2S 64                     // 横のU2要素数 1024ドット/U2アクセス(16bit)=64個

// DMA関数群との型一致のため外部キャストでvolatile属性を意図的に解除
