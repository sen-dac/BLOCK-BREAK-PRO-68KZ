//============================================================================================
// 指定した時間処理を止める
//--------------------------------------------------------------------------------------------
// Arguments : S4 s4_wait : (I) ウェイト時間 (ms単位)
// Return    : -
//============================================================================================
VD vdf_g_TimerWait(S4 s4_wait)
{
    S4 s4_start;
    S4 s4_now;

    // 開始時間取得：ONTIME()の戻り値は10ms単位なので10倍してms単位の値にする
    s4_start = (ONTIME() * 10);

    while(1)
    {
        // 現在時間取得：ONTIME()の戻り値は10ms単位なので10倍してms単位の値にする
        s4_now =(ONTIME() * 10);

        // 現在時間より開始時間が大きい場合は、
        // ONTIME()の戻り値が23時間59分59秒99を超えて一度クリアされている。
        // なので、現在時間に24時間に相当する値(86400000ms)を加算する。
        if(s4_now < s4_start)
        {
            s4_now += 86400000; // ms
        }

        if((s4_now - s4_start) >= s4_wait) break;
    }
}

//============================================================================================
// ONTIME()で得たbin時間を 時,分,秒,ミリ秒 に分割する
//--------------------------------------------------------------------------------------------
// Arguments : stTIME* const stcp_t : (I) stTIME構造体のアドレス
// Return    : -
//============================================================================================
VD vdf_g_TimerSep(stTIME* const stcp_t)
{
    stcp_t->s4_h  =  stcp_t->s4_b / 3600 / 100;
    stcp_t->s4_m  = (stcp_t->s4_b - (stcp_t->s4_h * 3600 * 100)) / 6000;
    stcp_t->s4_s  = (stcp_t->s4_b - (stcp_t->s4_h * 3600 * 100) - (stcp_t->s4_m * 6000)) / 100;
    stcp_t->s4_ms = (stcp_t->s4_b % 100) * 10;
}
