//--------------------------------------------------------------------------------------------
// 定数定義
//--------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------
// ■ パレット番号(PB：パレットブロック) [0～15] (設定によらず枚数固定)
//--------------------------------------------------------------------------------------------
// テキスト画面はハード仕様通り PB_TXT_00 を使用する。
// SP/BG用(PCG)は衝突を避けるため PB_PCG_01 以降を割り当てる。
//--------------------------------------------------------------------------------------------
#define PB_TXT_TEXT      PB_TXT_00           // テキスト画面専用 (変更不可)
#define PB_PCG_BALL      PB_PCG_01           // ボール(SP)
#define PB_PCG_SHOT      PB_PCG_02           // ショット(SP)
#define PB_PCG_ITEM      PB_PCG_03           // アイテム(SP)
#define PB_PCG_RACKET    PB_PCG_04           // ラケット(SP)
#define PB_PCG_NUMBER    PB_PCG_05           // 数字(SP)
#define PB_PCG_CURSOR    PB_PCG_06           // タイトル画面カーソル(SP)
#define PB_PCG_BLOCK     PB_PCG_07           // ブロック(BG)

#define PB_PCG_RESERVE_0 PB_PCG_08           // リザーブ：空き領域
#define PB_PCG_RESERVE_1 PB_PCG_09           // リザーブ：空き領域
#define PB_PCG_RESERVE_2 PB_PCG_10           // リザーブ：空き領域
#define PB_PCG_RESERVE_3 PB_PCG_11           // リザーブ：空き領域
#define PB_PCG_RESERVE_4 PB_PCG_12           // リザーブ：空き領域
#define PB_PCG_RESERVE_5 PB_PCG_13           // リザーブ：空き領域
#define PB_PCG_RESERVE_6 PB_PCG_14           // リザーブ：空き領域
#define PB_PCG_RESERVE_7 PB_PCG_15           // リザーブ：空き領域

//--------------------------------------------------------------------------------------------
// ■ PCG番号 [0～191] (設定により枚数が変化)
//--------------------------------------------------------------------------------------------
// PCG最大枚数はBGコントロールレジスタ(Inside X68000 P.190)のBGn TXSELビットの設定による。
// BGを1面しか使用せず、BGデータエリア1しか使用しない場合、BGデータエリア0をPCGとして利用可能。
// (POPCOM編集部・編「X68000 データブック」の記載に基づく)
//--------------------------------------------------------------------------------------------
// 参考書籍：
// 「Inside X68000」      P.174 図...7
//                        P.176 図...8
// 「X68000 データブック」P.40  図 2.4.1
//
// 表記について：
// 以下は各参考書籍毎に表記が異なるが、同じ領域を指す。
// Inside X68000      ："BGデータエリア0" / "BGデータエリア1"
// X68000 データブック："TEXTエリア0"     / "TEXTエリア1"
//--------------------------------------------------------------------------------------------
// 設定一覧：
// (a).BGを2枚使用(BGデータエリア0,1を使用) → PCGは 0～127 の128枚が利用可
// (b).BGを1枚使用(BGデータエリア1のみ使用) → PCGは 0～191 の192枚が利用可 ← この設定を使用
// (c).BGを未使用 (BGデータエリアは未使用)  → PCGは 0～255 の256枚が利用可
//
// 注：BG0/BG1がそれぞれデータエリア0,1どちらを参照するかもレジスタ設定により異なる。
//--------------------------------------------------------------------------------------------
#define PCG_BLOCK_TOP    0                   // ブロック：先頭 上8種 * 下8種 = 計64枚
#define PCG_BLOCK_END    63                  // ブロック：末尾

#define PCG_NUMBER_TOP   64                  // 数字    ：先頭 0 ～ 9
#define PCG_NUMBER_END   73                  // 数字    ：末尾

#define PCG_ITEM_TOP     74                  // アイテム：先頭 4種
#define PCG_ITEM_END     77                  // アイテム：末尾

#define PCG_RESERVE_TOP  78                  // リザーブ：先頭 空き領域
#define PCG_RESERVE_END  186                 // リザーブ：末尾

#define PCG_CURSOR_TOP   187                 // カーソル：先頭 (単)
#define PCG_CURSOR_END   187                 // カーソル：末尾

#define PCG_BALL_TOP     188                 // ボール  ：先頭 (単)
#define PCG_BALL_END     188                 // ボール  ：末尾

#define PCG_SHOT_TOP     189                 // ショット：先頭 (単)
#define PCG_SHOT_END     189                 // ショット：末尾

#define PCG_RACKET_TOP   190                 // ラケット：先頭 (左右端末)
#define PCG_RACKET_END   191                 // ラケット：末尾 (末尾)

//--------------------------------------------------------------------------------------------
// ■ SPプレーン番号 [0～127] (設定によらず枚数固定)
//--------------------------------------------------------------------------------------------
// 注：ブロックはBG描画のため割り当てなし
//--------------------------------------------------------------------------------------------
#define SPPL_BALL_TOP    0                   // ボール  ：先頭 (10個)
#define SPPL_BALL_END    9                   // ボール  ：末尾

#define SPPL_SHOT_TOP    10                  // ショット：先頭 (10個)
#define SPPL_SHOT_END    19                  // ショット：末尾

#define SPPL_RACKET_TOP  20                  // ラケット：先頭 (4個)
#define SPPL_RACKET_END  23                  // ラケット：末尾

#define SPPL_STAGE_TOP   24                  // ステージ：先頭 (1桁)
#define SPPL_STAGE_END   24                  // ステージ：末尾

#define SPPL_SCORE_TOP   25                  // スコア  ：先頭 (6桁)
#define SPPL_SCORE_END   30                  // スコア  ：末尾

#define SPPL_COUNT_TOP   31                  // カウント：先頭 (6桁)
#define SPPL_COUNT_END   36                  // カウント：末尾

#define SPPL_ITEM_TOP    37                  // アイテム：先頭 (86個) 理論上はあと+10枚必要
#define SPPL_ITEM_END    122                 // アイテム：末尾 だが同時に出現する事はまず無い

#define SPPL_RESERVE_TOP 123                 // リザーブ：先頭 (5枚) 空き領域
#define SPPL_RESERVE_END 127                 // リザーブ：末尾 必要に応じ割り当てる

#define SPPL_CURSOR_TOP  (SPPL_BALL_TOP + 1) // カーソル：先頭 (2個)
#define SPPL_CURSOR_END  (SPPL_BALL_TOP + 2) // カーソル：末尾

