//============================================================================================
// ゲームオブジェクト[数字フォント]の描画処理
//--------------------------------------------------------------------------------------------
// Arguments : U1 u1_pl    : (I) プレーン番号
//             S2 s2_x     : (I) 表示座標 X
//             S2 s2_y     : (I) 表示座標 Y
//             S4 s4_val   : (I) 描画する値
//             U1 u1_digit : (I) 描画する値の桁数
// Return    : -
//============================================================================================
VD vdf_g_ObjNumfontDraw(U1 u1_pl, S2 s2_x, S2 s2_y, S4 s4_val, U1 u1_digit)
{
    S4 s4_i;
    U4 u4a_dbuf[DIGIT_BUFF_SIZE];                           // 桁分離格納用

    if(u1_digit > 0 && u1_digit <= DIGIT_BUFF_SIZE)         // 桁数が既定範囲内か？
    {
        vdf_g_ObjNumfontDigSep(s4_val, u4a_dbuf, u1_digit); // 本カウントの桁分離

        for(s4_i = 0; s4_i < u1_digit; s4_i ++ )            // カウント表示(上位桁から)
        {
            vdf_g_SpRegst(u1_pl + s4_i,                     // プレーン番号
                          s2_x + (s4_i * NFONT_WIDTH),      // X座標
                          s2_y,                             // Y座標
                          SP_V_INV_OFF,                     // V-垂直反転
                          SP_H_INV_OFF,                     // H-水平反転
                          PB_PCG_NUMBER,                    // パレットブロック番号
                          PCG_NUMBER_TOP + u4a_dbuf[s4_i],  // PCG番号
                          SP_PRW_SP_BG0_BG1);               // 描画優先度
        }
    }
}

