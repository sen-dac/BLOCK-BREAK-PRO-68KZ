//============================================================================================
// 桁分離処理
//--------------------------------------------------------------------------------------------
// Arguments : S4        s4_val   : (I) 桁分解対象の値
//             U4* const u4cp_buf : (O) 出力配列先頭アドレス(要素[0]から上位桁順に格納)
//             U1        u1_digit : (I) 桁数
// Return    : -
//============================================================================================
static VD vdf_g_ObjNumfontDigSep(S4 s4_val, U4* const u4cp_buf, U1 u1_digit)
{
    S4 s4_i;
    S4 s4_filter = 1;                             // フィルタ

    for(s4_i = 0; s4_i < (u1_digit - 1); s4_i ++) // 渡された桁数-1回ループ
    {
        s4_filter *= 10;                          // 渡された桁数の最小値になる
    }                                             // 例：u1_digitが6 → s4_filter = 100000

    for(s4_i = 0; s4_i < u1_digit; s4_i ++)       // 渡された桁数回ループ
    {
        u4cp_buf[s4_i] = s4_val / s4_filter;      // 最上位桁の値だけを抽出(小数点以下切捨)
        s4_val %= s4_filter;                      // 最上位桁を削除(次回ループは次の桁が最上位)
        s4_filter /= 10;                          // フィルタの桁もひとつ減らす
    }
}
