//============================================================================================
// ゲームオブジェクト[数字フォント]の画像とパレットをロード
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : BL : (O) TRUE  : 成功
//                      FALSE : 失敗
//============================================================================================
BL blf_g_ObjNumfontLoadImage(VD)
{
    S4  s4_i;
    U4  u4a_pcg[PCG_TILE_SIZE_32U4];                              // BMP → PCG変換用
    U2  u2a_pal[DR_COL16];                                        // 16色パレット登録用
    U1* const u1cp_bbuf = u1cpf_g_DrGetBmpBuf();                  // BMPロード用バッファ取得

    // 数字フォント(SP)-16色BMP画像ロードと書込み
    for(s4_i = 0; s4_i < NFONT_PCG_MAX; s4_i++)
    {
        // 16色BMP画像ロードと書込み
        if(!blf_g_DrLoadBmp16(szacpc_s_NumImgFileNameTbl[s4_i], u1cp_bbuf)) return FALSE;
        vdf_g_SpBmpToPcg(u4a_pcg, (U4*)u1cp_bbuf);                // BMP → PCG 変換
        SP_DEFCG(PCG_NUMBER_TOP + s4_i, SP_SIZE16, (U1*)u4a_pcg); // PCGに画像転送
    }
    vdf_g_AppPBarAdd();                                           // プログレスバーを進める

    // 数字フォント(SP)のパレットのロード(GR以外は登録をここで行っても良い)
    if(!blf_g_DrLoadPal16(szacpc_s_NumImgFileNameTbl[0], u2a_pal, BRIGHT_OFF)) return FALSE;

    vdf_g_DrSetPal16(DR_PAL_PCG, PB_PCG_NUMBER, u2a_pal);         // 登録
    vdf_g_AppPBarAdd();                                           // プログレスバーを進める

    return TRUE;
}

