#include "score.h"

//--------------------------------------------------------------------------------------------
// 定数定義 (内部用)
//--------------------------------------------------------------------------------------------
#define SCORE_DIGIT   6      // スコア表示の桁数
#define SCORE_CNSTOP  999999 // スコアのカンスト値(SCORE_DIGITに合わせて設定すること)

//--------------------------------------------------------------------------------------------
// 型定義 (内部用)
//--------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------
// プロトタイプ宣言 (static)
//--------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------
// グローバル変数定義 (外部公開)
//--------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------
// 静的変数定義 (static)
//--------------------------------------------------------------------------------------------
static S4 s4_s_ObjScoreScr = 0; // 現在のスコア

//--------------------------------------------------------------------------------------------
// 関数定義
//--------------------------------------------------------------------------------------------
//============================================================================================
// ゲームオブジェクト[スコア]の初期化処理
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//--------------------------------------------------------------------------------------------
// Note      :
// 他のオブジェクトと構造を合わせるために存在 (今後の拡張用)
//============================================================================================
VD vdf_g_ObjScoreInit(VD)
{
    // NOP
}

//============================================================================================
// ゲームオブジェクト[スコア]の実行処理
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//--------------------------------------------------------------------------------------------
// Note      :
// 他のオブジェクトと構造を合わせるために存在 (今後の拡張用)
//============================================================================================
VD vdf_g_ObjScoreRun(VD)
{
    // NOP
}

//============================================================================================
// ゲームオブジェクト[スコア]の描画処理
//--------------------------------------------------------------------------------------------
// Arguments : S2 s2_x : (I) 表示座標 X
//             S2 s2_y : (I) 表示座標 Y
// Return    : -
//============================================================================================
VD vdf_g_ObjScoreDraw(S2 s2_x, S2 s2_y)
{
    vdf_g_ObjNumfontDraw(SPPL_SCORE_TOP, s2_x, s2_y, s4_s_ObjScoreScr, SCORE_DIGIT);
}

//============================================================================================
// ゲームオブジェクト[スコア]の解放処理
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//--------------------------------------------------------------------------------------------
// Note      :
// 初期化はシーン開始時に呼ばないが、こちらはシーン終了時に呼ぶこと.
//============================================================================================
VD vdf_g_ObjScoreRelease(VD)
{
    S4 s4_i;

    for(s4_i = 0; s4_i < SCORE_DIGIT; s4_i ++) // スコアのスプライト表示を全て消す
    {
        vdf_g_SpHide(SPPL_SCORE_TOP + s4_i);
    }
}

//============================================================================================
// スコアの加算
//--------------------------------------------------------------------------------------------
// Arguments : S4 s4_add : (I) 加算するスコア値
// Return    : -
//============================================================================================
VD vdf_g_ObjScoreAdd(S4 s4_add)
{
    s4_s_ObjScoreScr += s4_add;

    if(s4_s_ObjScoreScr > SCORE_CNSTOP)  // 上限値を超えた場合
    {
        s4_s_ObjScoreScr = SCORE_CNSTOP; // カンストとする
    }
}

//============================================================================================
// スコアを任意の値でセット (主にゼロクリア用)
//--------------------------------------------------------------------------------------------
// Arguments : S4 s4_score : (I) セットするスコア値
// Return    : -
//--------------------------------------------------------------------------------------------
// Note      :
// シーン[タイトル]の初期化処理で1回呼ぶこと(シーン[プレイ]の初期化処理で呼んではならない)
//============================================================================================
VD vdf_g_ObjScoreSet(S4 s4_score)
{
    s4_s_ObjScoreScr = s4_score;
}
