#include "stage.h"

//--------------------------------------------------------------------------------------------
// 定数定義 (内部用)
//--------------------------------------------------------------------------------------------
#define STAGE_DIGIT 1            // ステージ表示の桁数

//--------------------------------------------------------------------------------------------
// 型定義 (内部用)
//--------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------
// プロトタイプ宣言 (static)
//--------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------
// グローバル変数定義 (外部公開)
//--------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------
// 静的変数定義 (static)
//--------------------------------------------------------------------------------------------
static U1 u1_s_CurrentStage = 0; // 現在のステージ

//--------------------------------------------------------------------------------------------
// 関数定義
//--------------------------------------------------------------------------------------------
//============================================================================================
// ゲームオブジェクト[ステージ表示]の初期化処理
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//--------------------------------------------------------------------------------------------
// Note      :
// 他のオブジェクトと構造を合わせるために存在 (今後の拡張用)
//============================================================================================
VD vdf_g_ObjStageInit(VD)
{
    // NOP
}

//============================================================================================
// ゲームオブジェクト[ステージ表示]の実行処理
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//--------------------------------------------------------------------------------------------
// Note      :
// 他のオブジェクトと構造を合わせるために存在 (今後の拡張用)
//============================================================================================
VD vdf_g_ObjStageRun(VD)
{
    // NOP
}

//============================================================================================
// ゲームオブジェクト[ステージ表示]の描画処理
//--------------------------------------------------------------------------------------------
// Arguments : S2 s2_x : (I) 表示座標 X
//             S2 s2_y : (I) 表示座標 Y
// Return    : -
//============================================================================================
VD vdf_g_ObjStageDraw(S2 s2_x, S2 s2_y)
{
    U1 u1_stage = u1_s_CurrentStage + 1; // ステージ1は値0なので表示のため + 1 する

    vdf_g_ObjNumfontDraw(SPPL_STAGE_TOP, s2_x, s2_y, u1_stage, STAGE_DIGIT);
}

//============================================================================================
// ゲームオブジェクト[ステージ表示]の解放処理
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//============================================================================================
VD vdf_g_ObjStageRelease(VD)
{
    vdf_g_SpHide(SPPL_STAGE_TOP);        // ステージ表示 消去
}

//============================================================================================
// ステージをインクリメント
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : BL bl_ret : (O) TRUE  : 最大ステージ数を超えた
//                             FALSE : 最大ステージ数を超えていない
//============================================================================================
BL blf_g_ObjStageAdd(VD)
{
    BL bl_ret = FALSE;                         // 戻り値格納用

    u1_s_CurrentStage ++;                      // 次のステージへ

    if(u1_s_CurrentStage >= STAGE_MAX)         // 上限を超えたら
    {
        u1_s_CurrentStage = STAGE_MAX - 1;     // 最大値とする

        bl_ret = TRUE;                         // 最大ステージを超えた
    }

    return bl_ret;
}

//============================================================================================
// ステージを任意の値でセット
//--------------------------------------------------------------------------------------------
// Arguments : U1 u1_stage : (I) セットするステージ番号
// Return    : -
//============================================================================================
VD vdf_g_ObjStageSet(U1 u1_stage)
{
    if(u1_stage < STAGE_MAX) u1_s_CurrentStage = u1_stage;
}

//============================================================================================
// 現在のステージを返す
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : U1 u1_s_CurrentStage : (I) 現在のステージ
//============================================================================================
U1 u1f_g_ObjStageGet(VD)
{
    return u1_s_CurrentStage;
}
