#include "count.h"

//--------------------------------------------------------------------------------------------
// 定数定義 (内部用)
//--------------------------------------------------------------------------------------------
#define COUNT_DIGIT     6     // カウント表示の桁数
#define COUNT_INIVAL    10000 // カウントダウン初期値(既定の桁数以下であること)
#define COUNT_PRE_FRAME 10    // プレカウントによる本カウント動作までのフレーム数

//--------------------------------------------------------------------------------------------
// 型定義 (内部用)
//--------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------
// プロトタイプ宣言 (static)
//--------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------
// グローバル変数定義 (外部公開)
//--------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------
// 静的変数定義 (static)
//--------------------------------------------------------------------------------------------
static S4 s4_s_ObjCountCnt    = COUNT_INIVAL; // 本カウント
static S4 s4_s_ObjCountPreCnt = 0;            // プレカウント
static BL bl_s_ObjCountValid  = FALSE;        // カウント動作状態(ON:TRUE / OFF:FALSE)

//--------------------------------------------------------------------------------------------
// 関数定義
//--------------------------------------------------------------------------------------------
//============================================================================================
// ゲームオブジェクト[カウント]の初期化処理
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//============================================================================================
VD vdf_g_ObjCountInit(VD)
{
    bl_s_ObjCountValid = FALSE;;        // カウント停止
    s4_s_ObjCountCnt    = COUNT_INIVAL; // 本カウント初期化
    s4_s_ObjCountPreCnt = 0;            // プレカウント初期化
}

//============================================================================================
// ゲームオブジェクト[カウント]の実行処理
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//============================================================================================
VD vdf_g_ObjCountRun(VD)
{
    if(bl_s_ObjCountValid)                         // カウント動作状態が ON なら
    {
        s4_s_ObjCountPreCnt ++;                    // プレカウントを進める

        if(s4_s_ObjCountPreCnt >= COUNT_PRE_FRAME) // プレカウントが既定の値に到達したら
        {
            s4_s_ObjCountPreCnt = 0;               // プレカウントをクリア
            s4_s_ObjCountCnt --;                   // 本カウントをカウントダウン

            if(s4_s_ObjCountCnt < 0)               // 本カウントの下限は0とする
            {
                s4_s_ObjCountCnt = 0;
            }
        }
    }
}

//============================================================================================
// ゲームオブジェクト[カウント]の描画処理
//--------------------------------------------------------------------------------------------
// Arguments : S2 s2_x : (I) 表示座標 X
//             S2 s2_y : (I) 表示座標 Y
// Return    : -
//============================================================================================
VD vdf_g_ObjCountDraw(S2 s2_x, S2 s2_y)
{
    vdf_g_ObjNumfontDraw(SPPL_COUNT_TOP, s2_x, s2_y, s4_s_ObjCountCnt, COUNT_DIGIT);
}

//============================================================================================
// ゲームオブジェクト[カウント]の解放処理
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//============================================================================================
VD vdf_g_ObjCountRelease(VD)
{
    S4 s4_i;

    for(s4_i = 0; s4_i < COUNT_DIGIT; s4_i ++) // カウントのスプライト表示を全て消す
    {
        vdf_g_SpHide(SPPL_COUNT_TOP + s4_i);
    }

    bl_s_ObjCountValid = FALSE;;               // カウント停止
}

//============================================================================================
// カウント開始
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//============================================================================================
VD vdf_g_ObjCountStart(VD)
{
    bl_s_ObjCountValid = TRUE;
}

//============================================================================================
// カウント取得
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//============================================================================================
S4 s4f_g_ObjCountGet(VD)
{
    return s4_s_ObjCountCnt;
}
