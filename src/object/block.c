//============================================================================================
// ゲームオブジェクト[ブロック]の画像とパレットをロード
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : BL : (O) TRUE  : 成功
//                      FALSE : 失敗
//============================================================================================
BL blf_g_ObjBlockLoadImage(VD)
{
    S4  s4_i;
    U4  u4a_pcg[PCG_TILE_SIZE_32U4];                     // BMP → PCG変換用
    U2  u2a_pal[DR_COL16];                               // 16色パレット登録用
    U1* const u1cp_bbuf = u1cpf_g_DrGetBmpBuf();         // BMPロード用バッファ取得

    for(s4_i = 0; s4_i < BLOCK_BMP_FILE_NUM; s4_i++)     // 全てのブロック画像ファイル
    {
        // 16色BMP画像ロードと書込み
        if(!blf_g_DrLoadBmp16(szacpc_s_BlockImgFileNameTbl[s4_i], u1cp_bbuf)) return FALSE;

        vdf_g_SpBmpToPcg(u4a_pcg, (U4*)u1cp_bbuf);       // BMP → PCG 変換
        SP_DEFCG(s4_i, SP_SIZE16, (U1*)u4a_pcg);         // PCGに画像転送
    }
    vdf_g_AppPBarAdd();                                  // プログレスバーを進める

    // ブロック(BG)の16色パレットをロード(GR以外は登録をここで行っても良い)
    if(!blf_g_DrLoadPal16(szacpc_s_BlockImgFileNameTbl[0], u2a_pal, BRIGHT_OFF)) return FALSE;

    vdf_g_DrSetPal16(DR_PAL_PCG, PB_PCG_BLOCK, u2a_pal); // 登録
    vdf_g_AppPBarAdd();                                  // プログレスバーを進める

    return TRUE;
}

//============================================================================================
// ゲームオブジェクト[ブロック]の初期化処理
//--------------------------------------------------------------------------------------------
// Arguments : U1 u1_stage : (I) 現在のステージ (ここで指定したステージのブロックデータになる)
// Return    : -
//============================================================================================
VD vdf_g_ObjBlockInit(U1 u1_stage)
{
    S4 s4_i, s4_x, s4_y;
    U1 u1_top;           // BG表示：上段ブロックデータ
    U1 u1_btm;           // BG表示：下段ブロックデータ
    U1 u1_pcg;           // PCG番号
    U2 u2_ptc;           // パターンコード

    if(u1_stage < STAGE_MAX)
    {
        // ブロックデータ初期化
        for(s4_i = 0; s4_i < BLOCK_DATA_EL_MAX; s4_i ++)
        {
            u1a_s_ObjBlockData[s4_i] = u1aac_s_ObjBlockDefData[u1_stage][s4_i];
        }

        // BG初期化(表示)
        // ブロック1つの大きさW:32,H:8に対して、BGは16*16サイズ(個別座標変更不可グリッド固定)
        // ブロック種別は"0:なし"を含めて0-7の計8種類がある
        // 上段ブロック(8種) * 下段ブロック(8種) = 64種類の画像(PCG)が存在する。
        for(s4_y = 0; s4_y < BLOCK_BG_EL_H; s4_y ++){
            for(s4_x = 0; s4_x < BLOCK_BG_EL_W; s4_x += 2){

                // y * 2 にて、y方向は縦2段毎のアクセスを行う
                // x / 2 にて、x方向は 0.1.2.3... と順にアクセスする
                u1_top = // 上段(基準)
                u1a_s_ObjBlockData[(s4_y * 2 + 0) * BLOCK_DATA_EL_W + (s4_x / 2)];
                u1_btm = // 下段(y + 1)
                u1a_s_ObjBlockData[(s4_y * 2 + 1) * BLOCK_DATA_EL_W + (s4_x / 2)];

                // PCG番号算出(構造についてはszacpc_s_BlockImgFileNameTblを参照)
                u1_pcg = PCG_BLOCK_TOP + (u1_top * BLOCK_DATA_TYPE_MAX + u1_btm);

                // ブロック左
                u2_ptc = u2f_g_SpMkPtc(SP_V_INV_OFF, SP_H_INV_OFF, PB_PCG_BLOCK, u1_pcg);
                vdf_g_SpBgSet(SP_BG_AR1,
                              s4_x + BLOCK_X_OFFSET + 0,
                              s4_y + BLOCK_Y_OFFSET,
                              u2_ptc);

                // ブロック右：1つ右側のマスにH-水平反転で表示
                u2_ptc = u2f_g_SpMkPtc(SP_V_INV_OFF, SP_H_INV_ON, PB_PCG_BLOCK, u1_pcg);
                vdf_g_SpBgSet(SP_BG_AR1,
                              s4_x + BLOCK_X_OFFSET + 1,
                              s4_y + BLOCK_Y_OFFSET,
                              u2_ptc);
            }
        }
        vdf_g_SpBg0dispOn(); // BG0表示 ON
    }
}

