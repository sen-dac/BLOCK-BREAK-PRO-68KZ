//============================================================================================
// 指定箇所のブロック表示(BG)更新 (ブロックデータを基にBGの表示を更新する)
//--------------------------------------------------------------------------------------------
// Arguments : U2 u2_x : (I) BG表示更新対象ブロックデータのX要素番号
//             U2 u2_y : (I) BG表示更新対象ブロックデータのY要素番号
// Return    : -
//============================================================================================
VD vdf_g_ObjBlockBgUpdate(U2 u2_x, U2 u2_y)
{
    U1 u1_top;    // BG表示：上段ブロックデータ
    U1 u1_btm;    // BG表示：下段ブロックデータ
    U1 u1_pcg;    // PCG番号
    U4 u2_ptc;    // パターンコード

    if(u2_x < BLOCK_DATA_EL_W && u2_y < BLOCK_DATA_EL_H)                      // 引数範囲確認
    {
        // u2_yが偶数の場合(BG内上段のブロックが基準)
        if(!(u2_y & 1))
        {
            u1_top = u1a_s_ObjBlockData[(u2_y + 0) * BLOCK_DATA_EL_W + u2_x]; // 上段(基準)
            u1_btm = u1a_s_ObjBlockData[(u2_y + 1) * BLOCK_DATA_EL_W + u2_x]; // 下段(y + 1)

            // PCG番号算出(構造についてはszacpc_s_BlockImgFileNameTblを参照)
            u1_pcg = PCG_BLOCK_TOP + (u1_top * BLOCK_DATA_TYPE_MAX + u1_btm);

            // ブロック左
            u2_ptc = u2f_g_SpMkPtc(SP_V_INV_OFF, SP_H_INV_OFF, PB_PCG_BLOCK, u1_pcg);
            vdf_g_SpBgSet(SP_BG_AR1,
                          (u2_x * 2) + BLOCK_X_OFFSET + 0,
                          (u2_y / 2) + BLOCK_Y_OFFSET,
                          u2_ptc);

            // ブロック右：一つ右側のマスにH反転(u1_h1)表示
            u2_ptc = u2f_g_SpMkPtc(SP_V_INV_OFF, SP_H_INV_ON, PB_PCG_BLOCK, u1_pcg);
            vdf_g_SpBgSet(SP_BG_AR1,
                          (u2_x * 2) + BLOCK_X_OFFSET + 1,
                          (u2_y / 2) + BLOCK_Y_OFFSET,
                          u2_ptc);
        }
        // u2_yが奇数の場合(BG内下段のブロックが基準)
        else
        {
            u1_top = u1a_s_ObjBlockData[(u2_y - 1) * BLOCK_DATA_EL_W + u2_x]; // 上段(y - 1)
            u1_btm = u1a_s_ObjBlockData[(u2_y - 0) * BLOCK_DATA_EL_W + u2_x]; // 下段(基準)

            // PCG番号算出(構造についてはszacpc_s_BlockImgFileNameTblを参照)
            u1_pcg = PCG_BLOCK_TOP + (u1_top * BLOCK_DATA_TYPE_MAX + u1_btm);

            // ブロック左
            u2_ptc = u2f_g_SpMkPtc(SP_V_INV_OFF, SP_H_INV_OFF, PB_PCG_BLOCK, u1_pcg);
            vdf_g_SpBgSet(SP_BG_AR1,
                          (u2_x * 2) + BLOCK_X_OFFSET + 0,
                          ((u2_y - 1) / 2) + BLOCK_Y_OFFSET,
                          u2_ptc);

            // ブロック右：一つ右側のマスにH反転(u1_h1)表示
            u2_ptc = u2f_g_SpMkPtc(SP_V_INV_OFF, SP_H_INV_ON, PB_PCG_BLOCK, u1_pcg);
            vdf_g_SpBgSet(SP_BG_AR1,
                          (u2_x * 2) + BLOCK_X_OFFSET + 1,
                          ((u2_y - 1) / 2) + BLOCK_Y_OFFSET,
                          u2_ptc);
        }
    }
}

//============================================================================================
// ブロックを全て崩したか？ (クリア判定)
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : BL bl_ret : (O) TRUE  : ブロックを全て崩した
//                             FALSE : ブロックはまだある
//============================================================================================
BL blf_g_ObjBlockIsAllNone(VD)
{
    S4 s4_i;
    BL bl_ret = TRUE;                                // 戻り値格納用

    for(s4_i = 0; s4_i < BLOCK_DATA_EL_MAX; s4_i ++) // 全てのブロックの中に
    {
        if(u1a_s_ObjBlockData[s4_i])                 // まだ消えていないブロックがあるなら
        {
            bl_ret = FALSE;                          // クリアではない
            break;                                   // 1つでもあれば確定なのでループ終了
        }
    }

    return bl_ret;
}

//============================================================================================
// ブロックデータと表示を全クリア
//--------------------------------------------------------------------------------------------
// Arguments : -
// Return    : -
//============================================================================================
VD vdf_g_ObjBlockAllClear(VD)
{
    S4 s4_i, s4_x, s4_y;

    // ブロックデータ全クリア
    for(s4_i = 0; s4_i < BLOCK_DATA_EL_MAX; s4_i ++)
    {
        u1a_s_ObjBlockData[s4_i] = BLOCK_DATA_NONE;
    }

    // BG表示を全て消す
    for(s4_y = 0; s4_y < BLOCK_BG_EL_H; s4_y ++){
        for(s4_x = 0; s4_x < BLOCK_BG_EL_W; s4_x += 2){
            vdf_g_SpBgSet(SP_BG_AR1, s4_x + BLOCK_X_OFFSET + 0, s4_y + BLOCK_Y_OFFSET, SP_PRW_HIDE);
            vdf_g_SpBgSet(SP_BG_AR1, s4_x + BLOCK_X_OFFSET + 1, s4_y + BLOCK_Y_OFFSET, SP_PRW_HIDE);
        }
    }
}
