//============================================================================================
// 指定箇所のブロックデータを返す
//--------------------------------------------------------------------------------------------
// Arguments : U2 u2_x : (I) 取得対象ブロックデータのX要素番号
//             U2 u2_y : (I) 取得対象ブロックデータのY要素番号
// Return    : U1      : (O) ブロックデータ
//============================================================================================
U1 u1f_g_ObjBlockGetData(U2 u2_x, U2 u2_y)
{
    U1 u1_ret = 0;

    if(u2_x < BLOCK_DATA_EL_W && u2_y < BLOCK_DATA_EL_H)            // 引数は範囲内か？
    {
        u1_ret = u1a_s_ObjBlockData[u2_y * BLOCK_DATA_EL_W + u2_x]; // ブロックデータ取得
    }

    return u1_ret;
}

